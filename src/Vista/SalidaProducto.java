    public void actualizarTabla(){
        
        String matrix[][] = new String[restaurante.getProduccion().getInventario().size()][3] ;
        for( int i = 0 ; i < restaurante.getProduccion().getInventario().size() ; i++){
            int j = i+1 ;
            matrix[i][0] = Integer.toString(j) ;
            matrix[i][1] = restaurante.getProduccion().getInventario().get(i).nombre ;
            matrix[i][2] = Integer.toString(restaurante.getProduccion().getInventario().get(i).cantidad) ;
        }
        TablaSalida.setModel(new javax.swing.table.DefaultTableModel(
            matrix,
            new String [] {
                "# item", "Nombre", "Cantidad"
            }
        ) {
            Class[] types = new Class [] {
                java.lang.Integer.class, java.lang.String.class, java.lang.Integer.class
            };

        });
    }

