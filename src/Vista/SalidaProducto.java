    private void buttonColoredAction7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonColoredAction7ActionPerformed
        // TODO add your handling code here:
        try{

            this.item = Integer.parseInt(itemtxt.getText())-1 ;
            this.cantidad = Integer.parseInt(cantidadtxt.getText()) ;
            if(restaurante.getProduccion().getInventario().get(item).cantidad - this.cantidad < 0 ){
                JOptionPane.showMessageDialog(null, "No hay productos suficientes");
            }
            else{
                restaurante.getProduccion().getInventario().get(item).cantidad-= this.cantidad ;
                salida.add(restaurante.getProduccion().getInventario().get(item)) ;
                restaurante.ventas += restaurante.getProduccion().getInventario().get(item).cantidad * restaurante.getProduccion().getInventario().get(item).getPrecio() ;
            }
            
        }
        catch( NumberFormatException ex ){
            System.out.println("NumberFormatException");
        }
        catch( ArrayIndexOutOfBoundsException ex){
            System.out.println("ArrayIndexOutOfBoundsException");
        }
        catch(NullPointerException ex){
            System.out.println("NullPointerException");
        }

        //System.out.println(this.item + " " + this.cantidad);
        //System.out.println(matrix[this.item][2]);
        actualizarTabla();
    }//GEN-LAST:event_buttonColoredAction7ActionPerformed

