    public void moverRobotcaja() throws InterruptedException{
        for( int i = 0 ; i < restaurante.getEntradaProductos().size() ; i++){
            if( restaurante.getEntradaProductos().get(i).isCongelado() || restaurante.getEntradaProductos().get(i).isRefrigerado()){
                robotsito.move(470, 400);
                Thread.sleep(1000);
                robotsito.move(470, 200);
            }
            if( restaurante.getEntradaProductos().get(i).isTempAmbiente()){
                robotsito.move(230, 400);
                Thread.sleep(1000);
                robotsito.move(230, 200);
            }
            
        }
        
        
    }
    
